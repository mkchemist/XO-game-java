package xo.app;

/**
 *
 * @author mk_ch
 */
public class Player {
    String name;
    String sign;
    
    public Player(String name,String sign){
        this.name = name;
        this.sign = sign;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSign() {
        return sign;
    }

    public void setSign(String sign) {
        this.sign = sign;
    }
    
}
